package ca.sheridancollege.project;

import java.util.ArrayList;

/**
 * The class that models your game. You should create a more specific
 * child of this class and instantiate the methods given.
 * We create Go Fish Cards Game, which contain 52 cards and at least 2 or more players 
 * The rules is, if there are 2 players each of them will get 7 cards, if there are 3 or more players 
 * each of them will get 5 cards and the remaining cards place in the middle . One of the player will
 * call the name of the card that person has if all others players have it then should give it to the 
 * first player for those cards that player put it down and then keep going to next player until 
 * they are all run out of cards. Final step that all players count how many cards each of them have
 * the player has the most cards is the winner.
 * @author dancye, 2018
 */
public abstract class Game 
{
    private final String gameName;//the title of the game
    private ArrayList <Player> players;// the players of the game
    
    public Game(String givenName)
    {
        gameName = givenName;
        players = new ArrayList();
    }

    /**
     * @return the gameName
     */
    public String getGameName() 
    {
        return gameName;
    }
    
     /**
     * @return the players of this game
     */
    public ArrayList <Player> getPlayers() 
    {
        return players;
    }

    /**
     * @param players the players of this game
     */
    public void setPlayers(ArrayList <Player> players) 
    {
        this.players = players;
    }
    
    /**
     * Play the game. This might be one method or many method calls depending
     * on your game.
     */
    public abstract void play();
    
    /**
     * When the game is over, use this method to declare and display a winning
     * player.
     */
    public abstract void declareWinner();

   
    
}//end class
